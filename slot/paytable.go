package slot

// Paytable 定義了各個符號的中獎規則
// 格式為 [符號][連線數] = 中獎分數
// 外層 map：以字串為 key（符號名稱，例如 "A"、"Wild"）
// 內層 map：key 是連線數（3、4、5），value 是對應的得分
// Wild	應可替代任何非特殊符號（除了 Scatter/Bonus）
// Bonus/Scatter	不應出現在 Paytable，通常在獨立邏輯中判斷
// 各符號數值間差距大	增加中獎起伏，創造娛樂刺激感
var Paytable = map[string]map[int]int{
	// 高價符號（角色或特別圖示）
	"Wild":   {3: 50, 4: 200, 5: 500},
	"Bonus":  {3: 0, 4: 0, 5: 0}, // 另有機制觸發 Free Spin，不直接得分
	"Dragon": {3: 30, 4: 100, 5: 300},
	"Tiger":  {3: 25, 4: 75, 5: 200},

	// 低價符號（撲克牌數字）
	"A": {3: 10, 4: 25, 5: 100},
	"K": {3: 8,  4: 20, 5: 80},
	"Q": {3: 6,  4: 15, 5: 60},
	"J": {3: 5,  4: 10, 5: 50},
	"10": {3: 4, 4: 8,  5: 40},
	"9":  {3: 3, 4: 6,  5: 30},
}

